# This is a comment - Python ignores everything after the # symbol
# Comments help you and others understand what your code does

print("Hello, Python World! üåç")
print("Welcome to your coding journey!")

# Variables are like containers that store data
# In Python, you don't need to declare the type - Python figures it out!

# Let's create some variables
name = "Python Learner"  # This is a string (text)
age = 25                # This is an integer (whole number)
height = 5.9           # This is a float (decimal number)
is_learning = True      # This is a boolean (True or False)

# Let's print our variables
print(f"My name is {name}")
print(f"I am {age} years old")
print(f"My height is {height} feet")
print(f"Am I learning Python? {is_learning}")

# Python is case-sensitive!
# 'Name' and 'name' are different variables
Name = "Different Name"
print(f"name = {name}")
print(f"Name = {Name}")

# You can reassign variables
age = 26  # Changed from 25 to 26
print(f"Next year I'll be {age}")

# Multiple assignment in one line
x, y, z = 1, 2, 3
print(f"x = {x}, y = {y}, z = {z}")

# You can also assign the same value to multiple variables
a = b = c = 10
print(f"a = {a}, b = {b}, c = {c}")

